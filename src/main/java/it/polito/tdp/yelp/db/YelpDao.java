	public List<Arco> getArchi(int anno, String city, Map<String, Business> mappa){
		String sql = "SELECT t1.`business_id` AS b1, t2.`business_id` AS b2, (t1.media-t2.media) AS aver "
				+ "FROM (SELECT  b.`business_id`, b.`city`, AVG(r.`stars`) AS media "
				+ "		FROM Business b, Reviews r "
				+ "		WHERE b.`business_id`=r.`business_id` "
				+ "		AND b.`city`=? "
				+ "		AND YEAR(r.`review_date`)=? "
				+ "		GROUP BY b.`business_id`) AS t1, "
				+ "		(SELECT  b.`business_id`, b.`city`, AVG(r.`stars`) AS media "
				+ "		FROM Business b, Reviews r "
				+ "		WHERE b.`business_id`=r.`business_id` "
				+ "		AND b.`city`=? "
				+ "		AND YEAR(r.`review_date`)=? "
				+ "		GROUP BY b.`business_id`) AS t2 "
				+ "WHERE t1.`city`=t2.`city` "
				+ "AND t1.`business_id`<t2.`business_id` "
				+ "HAVING aver<>0";
		List<Arco> result = new ArrayList<>();
		Connection conn = DBConnect.getConnection();

		try {
			PreparedStatement st = conn.prepareStatement(sql);
			st.setString(1, city);
			st.setInt(2, anno);
			st.setString(3, city);
			st.setInt(4, anno);
			ResultSet res = st.executeQuery();
			while (res.next()) {

				Arco arco = new Arco(mappa.get(res.getString("b1")), mappa.get(res.getString("b2")), res.getDouble("aver"));
				result.add(arco);
				System.out.println(arco.getPeso());
			}
			res.close();
			st.close();
			conn.close();
			return result;
			
		} catch (SQLException e) {
			e.printStackTrace();
			return null;
		}
	}
